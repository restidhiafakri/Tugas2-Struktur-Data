# Komponen : File Dokumen
# Dokumen akan memiliki atribut _content yang akan berisi semua karakter dalam dokumen.
# Metodenya yaitu :
# 1. delete(): menghapus karakter dari _content
# 2. insert(): menyisipkan karakter ke posisi tertentu dalam urutan
# 3. paste(): ini akan memasukkan isi _clipboard ke posisi dan rentang yang ditentukan di _content
# 4. copy(): ini akan menyalin urutan karakter tertentu dan menyimpannya di beberapa atribut _clipboard